                                <h3 class="title pt-35 pb-12">Identifica las áreas de paso clave</h3>
                                <p>
                                    Antes de comenzar a iluminar, es importante identificar las áreas de paso clave en
                                    el exterior. Esto puede incluir caminos, escaleras, puertas, entradas y salidas. Al
                                    identificar estas áreas, podrás planificar mejor dónde colocar las luminarias y qué
                                    tipo de iluminación se necesita.
                                </p>

                                <div class="bs-img">
                                    <img src="assets/images/blog/post/zonas-del-exterior2.jpg" alt="Blog">
