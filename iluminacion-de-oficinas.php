                                    una iluminación adecuada y eficiente.
                                </p>


                                <h3 class="title pt-35 pb-12">Planificación de la iluminación</h3>
                                <p>
                                    Antes de comenzar a instalar las luminarias, es importante hacer un plan de
                                    iluminación. En este plan se debe tener en cuenta la distribución del espacio, el
                                    número de empleados, el tipo de trabajo que se realiza, las horas de trabajo y el
                                    tipo de iluminación necesario. Además, también es importante considerar la luz
                                    natural y la cantidad de luz natural que entra en la oficina.
                                </p>

                                <div class="bs-img">
                                    <img src="assets/images/blog/post/luz-empotrada-oficina-300x202.jpg" alt="Blog">
