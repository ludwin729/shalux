                                <h3 class="title pt-35 pb-12">Iluminación general</h3>
                                <p>
                                    La iluminación general es importante para la iluminación uniforme de todo el
                                    espacio. Una buena opción para la iluminación general son los paneles LED que se
                                    colocan en el techo, proporcionando una luz suave y uniforme en todo el espacio.
                                </p>

                                <h3 class="title pt-35 pb-12">Iluminación de tareas</h3>
                                <p>
                                    La iluminación de tareas es importante para iluminar áreas específicas de trabajo,
                                    como escritorios o áreas de reuniones. La iluminación de tareas debe ser lo
                                    suficientemente brillante para permitir una buena visibilidad, pero no tan brillante
                                    que cause fatiga ocular. Las lámparas de escritorio LED son una buena opción para la
                                    iluminación de tareas, ya que proporcionan una luz brillante y enfocada.
                                </p>
                                <div class="bs-img">
                                    <img src="assets/images/blog/post/12_NeondeLuz2Office.jpg" alt="Blog">
                                </div>
                                <h3 class="title pt-35 pb-12">Iluminación de acento</h3>
                                <p>
                                    La iluminación de acento se utiliza para resaltar ciertos elementos o áreas de la
                                    oficina, como obras de arte, estanterías o elementos arquitectónicos. La iluminación
                                    de acento puede crear un ambiente más acogedor y cómodo para los trabajadores. Las
                                    luces LED empotradas en el techo son una buena opción para la iluminación de acento.
                                </p>
                                <h3 class="title pt-35 pb-12">Control de la iluminación</h3>
                                <p>
                                    El control de la iluminación es importante para ajustar la cantidad de luz en
                                    función de las necesidades de los trabajadores y para ahorrar energía. Los
                                    reguladores de intensidad y los sensores de movimiento son buenas opciones para el
                                    control de la iluminación. Los sensores de movimiento pueden detectar la presencia
                                    de personas en la oficina y ajustar la iluminación en consecuencia, mientras que los
                                    reguladores de intensidad permiten ajustar la cantidad de luz según las necesidades.
                                </p>
                                <h3 class="title pt-35 pb-12">Eficiencia energética</h3>
                                <p>
                                    La eficiencia energética es importante en la iluminación de oficinas, ya que puede
                                    reducir los costos de energía y reducir la huella de carbono de la oficina. Una
                                    buena opción para la eficiencia energética son los focos LED, que utilizan menos
                                    energía y duran más que los focos incandescentes o fluorescentes.
                                </p>
                                <h3 class="title pt-35 pb-12">Conclusión</h3>
